package entpack.bean;

public class CalcInfo {

	protected float ptReal = 0f;
	protected float ptRealUp = 0f;
	protected float downPtReal = 0f;


	// 计算上下级占成

	public Float getPtReal() {
		return ptReal;
	}

	public void setPtReal(float ptReal) {
		this.ptReal = ptReal;
	}

	public Float getPtRealUp() {
		return ptRealUp;
	}

	public void setPtRealUp(float ptRealUp) {
		this.ptRealUp = ptRealUp;
	}
	public Float getDownPtReal() {
		return downPtReal;
	}

	public void setDownPtReal(float downPtReal) {
		this.downPtReal = downPtReal;
	}
}
